// Пример кода с использованием динамической типизации в JS


// variable "car" declared but undefined
let car;
console.log(typeof car);

// variable "car" change data type to string
car = 'Mercedes'; 
console.log(typeof car);

// variable "car" change data type to number
car = 10;
console.log(typeof car);

// variable "car" change data type to bolean
car =  true;
console.log(typeof car);